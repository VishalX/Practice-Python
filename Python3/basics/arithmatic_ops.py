# Using Arithmatic Opereations
print('Python Basics : Arithmatic Operations')

# Define variables
var1 = 10
var2 = 20

# Addition
sum_of_vars = var1 + var2
print("var1 =", var1, "\tvar2 =", var2, "\tSum(var1 + var2) = ", sum_of_vars)

# Subtraction
sub_of_vars = var1 - var2
print("var1 =", var1, "\tvar2 =", var2, "\tSub(var1 - var2) = ", sub_of_vars)

# Multiplication
mul_of_vars = var1 * var2
print("var1 =", var1, "\tvar2 =", var2, "\tMul(var1 * var2) = ", mul_of_vars)

# Integer Division : Truncated Division (use // for any integer division)
div_of_vars = var1 // var2
print("var1 =", var1, "\tvar2 =", var2, "\tDiv(var1 / var2) = ", div_of_vars)

# Change variable values
var1 = 11
var2 = 3
div_of_vars = var1 // var2
print("var1 =", var1, "\tvar2 =", var2, "\tDiv(var1 / var2) = ", div_of_vars)

# Try with negative numbers : truncated to negative infinity
var1 = -11
div_of_vars = var1 // var2
print("var1 =", var1, "\tvar2 =", var2, "\tDiv(var1 / var2) = ", div_of_vars)

# Floating Point Division (use with /)
var1 = 11
var2 = 3
div_of_vars = var1 / var2
print("var1 =", var1, "\tvar2 =", var2, "\tDiv(var1 / var2) = ", div_of_vars)