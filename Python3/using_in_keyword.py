# Use in to find a num in list
numbers = [10, 20, 30, 40, 50]
number1 = 25
number2 = 40
# Check if number1 and number2 is present in list numbers 
if number1 in numbers:
    print("Present :", number1)
else:
    print("Not Present :", number1)

if number2 in numbers:
    print("Present :", number2)
else:
    print("Not Present :", number2)