# Looping using while
print('\nLooping using while')

# Define an iteration variable
itvar = 10
# Loop over some statement using while
print('\nInitializing Launch Sequence ...')
while itvar > 0:
    print(itvar) #itvar
    itvar = itvar - 1

print('* BOOM * Liftoff !\n')

# Infinite Loop
while True: # Always true
    print('Keep going on')

# No-trip Loop
while False:
    print('Keep going on')

